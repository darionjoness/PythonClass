radius = float(input("Enter the radius of the sphere: "))

diameter = 2 * radius

circumference = 2 * 3.14 * radius

surfaceArea = 4 * 3.14

volume = (4/3) * 3.14 * (radius ** 3)

print("Diameter: ", diameter)
print("Circumference: ", circumference)
print("surfaceArea: ", surfaceArea)
print("volume: ", volume)
